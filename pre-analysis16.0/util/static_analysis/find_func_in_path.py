        visited.add(start_node)
        for callee_node in final_map_no_cycle[start_node]:
            if not is_ignored_func(callee_node):
                if callee_node == root_node:
                    node_list.append(start_node)
                    # Sort node_list and deduplicate paths
                    node_list_sorted=sorted(node_list)
                    path_list.add(node_list)
                    path_list_sorted.add(node_list_sorted)
                    # for node in node_list:
                    #     node_weight[node]=node_weight.get(node,0)+1
                    # node_weight[start_node]=node_weight.get(start_node,0)+1
                    result=True
                    return result
                else:
                    node_list.append(start_node)
                    result=dfs_count_node(root_node,callee_node,visited,node_list,path_list)
