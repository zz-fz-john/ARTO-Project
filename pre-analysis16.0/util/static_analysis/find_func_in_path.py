"""
Assign a weight to each edge.
Weight definition:
    Traverse from a certain function node; if it can return to itself, then the weights of all points on this path are incremented by 1, and recursion stops.
Algorithm description:
    1. Perform deep-first search on each node; if it can return to itself, the weights of all points on this path are incremented by 1 and execution stops.
    2. If a previously visited node is encountered, return.
    3. If an unvisited node is encountered, add the node to the visited set, and then perform deep-first traversal on all its child nodes.
"""
