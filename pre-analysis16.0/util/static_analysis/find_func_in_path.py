def initialize_Ind_map(indirect_target_map_file):
    global final_map
    global indirect_map
    global indirectcallsite_map
    IRCallsite_pattern=re.compile(r"In function\s*:\s*([^\s]+)\s*indirect callsite\s*:\s*(.*)")
    target_pattern=re.compile(r'--target\s*(\S+)')
    IndirectCallsiteFunc=None
    with open(indirect_target_map_file,'r') as file:
        for line  in file:
            line= line.strip()
            IRcallsite_match=IRCallsite_pattern.match(line)
            target_match=target_pattern.match(line)
            if IRcallsite_match:
                IndirectCallsiteFunc=IRcallsite_match.group(1)
                Indirectcallsite=IRcallsite_match.group(2)
                if IndirectCallsiteFunc not in final_map:
                    final_map[IndirectCallsiteFunc]=set()
                if IndirectCallsiteFunc not in indirect_map:
                    indirect_map[IndirectCallsiteFunc]=set()
                if Indirectcallsite not in indirectcallsite_map:
                    indirectcallsite_map["ind-"+Indirectcallsite]=set()
            elif target_match:
                if target_match:
                    target_function=target_match.group(1)
                    if target_function not in final_map:
                        final_map[target_function]=set()
                    final_map[IndirectCallsiteFunc].add(target_function)
                    indirect_map[IndirectCallsiteFunc].add(target_function)
                    indirectcallsite_map["ind-"+Indirectcallsite].add(target_function)

