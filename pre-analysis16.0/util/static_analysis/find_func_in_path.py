def get_checkpoint_callsite(root_node,func_path_lenth=4,func_numbuer=16):
    global E
    global checkpoint_callsite
    global leaf_func
    path_length_map={}
    undirected_E = E.to_undirected()
    node_depth_map={} # minimum depth from leaf nodes to root node
    for func in leaf_func:
        if func not in undirected_E:
            continue
        # calculate the minimum depth from func to the root node
        try:
            depth = nx.shortest_path_length(E, source=root_node, target=func)
            node_depth_map[func]=depth
        except nx.NetworkXNoPath:
            node_depth_map[func]=sys.maxsize
    # Find the minimum distance between any two points in leaf_func in undirected_E, sorted by node_depth_map from smallest to largest to generate 46, or from largest to smallest to generate 45. Ascending order should be used because it processes shorter depths first, resulting in smaller evidence.
    sorted_leaf_func = sorted(leaf_func, key=lambda x: node_depth_map.get(x, sys.maxsize))
    for func in sorted_leaf_func:
        print(f"leaf_func: {func}, depth: {node_depth_map.get(func, 'N/A')}")
        if func not in undirected_E:
            continue
        for target in leaf_func:
            if target==func or target not in undirected_E:
                continue
            if (func,target) in path_length_map or (target,func) in path_length_map:
                continue
